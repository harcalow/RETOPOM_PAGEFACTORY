package com.orangehrmlive.opensourcedemo.model.admin.job.workshifts;

public class WorkshiftsModel {
    private String username;
    private String password;
    private String nameworkshift;
    private String hourFrom;
    private String hourTo;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getNameworkshift() {
        return nameworkshift;
    }

    public void setNameworkshift(String nameworkshift) {
        this.nameworkshift = nameworkshift;
    }

    public String getHourFrom() {
        return hourFrom;
    }

    public void setHourFrom(String hourFrom) {
        this.hourFrom = hourFrom;
    }

    public String getHourTo() {
        return hourTo;
    }

    public void setHourTo(String hourTo) {
        this.hourTo = hourTo;
    }

}
